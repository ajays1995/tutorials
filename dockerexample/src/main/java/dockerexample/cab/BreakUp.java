package dockerexample.cab;

public class BreakUp {
	
	private String type;
	private int baseFair;
	private int time;
	private double costPerdistance;
	private double surcharge;
	public String getType() {
		return type;
	}
	
	public void setType(String type) {
		this.type = type;
	}
	
	public int getBaseFair() {
		return baseFair;
	}
	
	public void setBaseFair(int baseFair) {
		this.baseFair = baseFair;
	}
	
	public int getTime() {
		return time;
	}
	
	public void setTime(int time) {
		this.time = time;
	}
	
	public double getCostPerdistance() {
		return costPerdistance;
	}
	
	public void setCostPerdistance(double costPerdistance) {
		this.costPerdistance = costPerdistance;
	}
	
	public double getSurcharge() {
		return surcharge;
	}
	
	public void setSurcharge(double surcharge) {
		this.surcharge = surcharge;
	}
	

}
